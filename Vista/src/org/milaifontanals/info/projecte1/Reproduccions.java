    private void AfegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AfegirActionPerformed
        if(jDateChooser_afegir.isValid() || jDateChooser_afegir==null){
            avis("DATA INVALIDA O INCOMPLETA");
            return;
        }
        Date dat = jDateChooser_afegir.getDate();
        Timestamp tstmp= new Timestamp(dat.getTime());
        
        BDClient c=new BDClient();
        String client = jComboBox_LlistaClients_afegir.getSelectedItem().toString();
        String[] parts = client.split(":");
        int id = Integer.parseInt(parts[0]);
        Client clie=null;
        try {
            clie=c.getClient(id);
        } catch (GestorBDSportifyException ex) {
            Logger.getLogger(Reproduccions.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String pr = jComboBox_LlistaProducte_afegir.getSelectedItem().toString();
        String[] partss = pr.split(":");
        int idd = Integer.parseInt(partss[0]);
        Producte producte=null;
        try {
            producte=prod.getProducte(idd);
        } catch (GestorBDSportifyException ex) {
            Logger.getLogger(Reproduccions.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         Reproduccio repoduccio_afegida=new Reproduccio(tstmp, clie,producte);
         
        try {
            rep.afegirReproduccio(repoduccio_afegida);
            missatge("Sha afegit una reproduccio!");
            setModelo();
            jDateChooser_afegir.cleanup();
        } catch (GestorBDSportifyException ex) {
            missatge("EPSSS!! ERROR");
            Logger.getLogger(Reproduccions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AfegirActionPerformed


    private void setDatos() {
        
        dtmRep.setNumRows(0);
        jTable1.setModel(dtmRep);
        String frag = jTextField_fragmentProd.getText();

        String client = jComboBox_LlistaClients.getSelectedItem().toString();
        String[] parts = client.split(":");
        int id = Integer.parseInt(parts[0]);

        rep = new BDReproduccio();
        Date inici = jCalendar_inici.getDate();
        Date ultima = jCalendar_fins.getDate();
        Object[] datos = new Object[dtmRep.getColumnCount()];

        try {
            repoduccions = rep.getListProducte(id, frag, new java.sql.Date(inici.getTime()), new java.sql.Date(ultima.getTime())/*new java.sql.Date(inici.getTime()), new java.sql.Date(ultima.getTime())/*/);

            for (int i = 0; i < repoduccions.size(); i++) {
                datos[0] = repoduccions.get(i).getClient().getNom();
                datos[1] = repoduccions.get(i).getProducte().getTitol();
                datos[2] = repoduccions.get(i).getTimestamp();
                dtmRep.addRow(datos);
            }
            missatge("Hem trobat: "+repoduccions.size()+ " reproduccions");
        } catch (Exception e) {
            missatge(e.getMessage());
            e.printStackTrace();
        }
        jTable1.setModel(dtmRep);
    }

